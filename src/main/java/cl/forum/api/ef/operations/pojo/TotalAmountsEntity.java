package cl.forum.api.ef.operations.pojo;

public class TotalAmountsEntity {
	
	private Integer montoTotalDisponible;
	private Integer montoTotalBloqueados;
	private Integer montoTotalVencidos;
	
	
	public Integer getMontoTotalDisponible() {
		return montoTotalDisponible;
	}
	public void setMontoTotalDisponible(Integer montoTotalDisponible) {
		this.montoTotalDisponible = montoTotalDisponible;
	}
	public Integer getMontoTotalBloqueados() {
		return montoTotalBloqueados;
	}
	public void setMontoTotalBloqueados(Integer montoTotalBloqueados) {
		this.montoTotalBloqueados = montoTotalBloqueados;
	}
	public Integer getMontoTotalVencidos() {
		return montoTotalVencidos;
	}
	public void setMontoTotalVencidos(Integer montoTotalVencidos) {
		this.montoTotalVencidos = montoTotalVencidos;
	}
	
	
}
